<h2 class="title">Politique de confidentialité</h2>	
<div class="clear"></div>
<div class="page-separator"></div>
<div class="post-content">

<h3>Collecte des renseignements personnels</h3>
<p>Nous collectons les renseignements suivants :</p>
<ul>
	<li>Nom et prénom</li>
    <li>Date de naissance</li>
    <li>Scolarité / Formation</li>
</ul>
<p>Les renseignements personnels que nous collectons sont recueillis lors du chargement de vos données depuis le système de gestion des études de l'université. Nous utilisons également, comme indiqué dans la section suivante, des fichiers témoins et/ou journaux pour réunir des informations vous concernant.</p>
<p>Nous utilisons les renseignements ainsi collectés pour l'affichage de vos données scolaires sur le site. En aucun cas ces données ne seront accessibles ou diffusées à un tiers sans votre consentement. De plus, <strong>les données mentionnées ci-haut ne seront jamais utilisées à des fins statistiques ou commerciales</strong>.</p>
<h3>Conservation des données</h3>
<p>Afin d'accélérer la consultation du site et d'améliorer la performance du service, certaines données personnelles sont conservées de façon sécurisée en mémoire cache sur le serveur. Ces données sont stockées sous forme cryptée et ne sont accessibles que par la connexion au site Web.</p>
<p>Les données d'historique et des pages visitées sont conservées à des fins statistiques, mais ne contiennent aucune donnée nominale.</p>
<h3>Protection de vos données</h3>
<p>Pour afficher vos données depuis le système de gestion des études de l'université, Pilule utilise des méthodes de connexion légales qui nécessite votre consentement préalable. <strong>En fournissant vos identifiants de connexion (IDUL et mot de passe) à Pilule, vous consentez donc à ce que le site accède à vos données sur le système de gestion des études.</strong> Pour des raisons de sécurité, ces identifiants de connexion ne sont mémorisés que pendant la durée de votre session sur Pilule.</p>
<p>Prenez note que Pilule ne peut en aucun cas accéder à ces données sans vos identifiants de connexion (IDUL). De plus, Pilule ne se connecte qu'une seule fois, lors de la connexion, au système de gestion des études pour récupérer vos données qui seront ensuite gardées en mémoire.</p>
<h3>Fichiers journaux et témoins</h3>
<p>Nous recueillons certaines informations par le biais de fichiers journaux (log file) et de fichiers témoins (cookies). Il s'agit principalement des informations suivantes :</p>
<ul>
	<li>Système d'exploitation</li>
    <li>Pages visitées et requêtes</li>
    <li>Heure et jour de connexion</li>
</ul>
<p>Le recours à de tels fichiers nous permet d'améliorer le service et sert surtout à des fins statistiques.</p>
<h3>Droit d'opposition et de retrait</h3>
<p>Nous nous engageons à vous offrir un droit d'opposition et de retrait quant à vos renseignements personnels.<br />
Le droit d'opposition s'entend comme étant la possiblité offerte aux internautes de refuser que leurs renseignements personnels soient utilisées à certaines fins mentionnées lors de la collecte.</p>
<p>Le droit de retrait s'entend comme étant la possiblité offerte aux internautes de demander à ce que leurs renseignements personnels ne figurent plus, par exemple, dans une liste de diffusion.</p>
<p>Pour pouvoir exercer ces droits, vous pouvez nous contacter à l'adresse suivante :</p>
<blockquote>
	Courriel : info@pilule.ca<br />
	Section du site web : https://www.pilule.ulaval.ca/
</blockquote>
<h3>Sécurité</h3>
<p>Les renseignements personnels que nous collectons sont conservés dans un environnement sécurisé. Les personnes travaillant pour nous sont tenues de respecter la confidentialité de vos informations.<br />
<p>Nous nous engageons à maintenir un haut degré de confidentialité en intégrant les dernières innovations technologiques permettant d'assurer la confidentialité de vos transactions. Toutefois, comme aucun mécanisme n'offre une sécurité maximale, une part de risque est toujours présente lorsque l'on utilise Internet pour transmettre des renseignements personnels.</p>
<h3>Déclaration de responsabilité</h3>
<p>Le service Pilule et ses créateurs se dégagent de toute responsabilité concernant l'utilisation du service.</p>
